Require Export
  Ty.

Open Scope ty_scope.

Inductive Builtin : Ty → Set :=
  (* IntOps *)
  (* Integer Add *)
  | AddInt           : Builtin (ℤ × ℤ)
  (* Int Num functions *)
  | SubInt           : Builtin (ℤ × ℤ)
  | DivInt           : Builtin (ℤ × ℤ)
  | MulInt           : Builtin (ℤ × ℤ)
  | NegateInt        : Builtin (ℤ × ℤ)
  | AbsInt           : Builtin (ℤ × ℤ)
  (* Int fractional *)
  | ExpInt           : Builtin (ℤ × ℤ)
  | LnInt            : Builtin (ℤ × ℤ)
  | SqrtInt          : Builtin (ℤ × ℤ)
  | LogBaseInt       : Builtin (ℤ × ℤ)
  (* General int ops *)
  | ModInt           : Builtin (ℤ × ℤ)
  | BitAndInt        : Builtin (ℤ × ℤ)
  | BitOrInt         : Builtin (ℤ × ℤ)
  | BitXorInt        : Builtin (ℤ × ℤ)
  | BitShiftInt      : Builtin (ℤ × ℤ)
  | BitComplementInt : Builtin (ℤ × ℤ)
  (* Int show instance *)
  | ShowInt          : Builtin (ℤ × ℤ)
  (* Int Equality *)
  | EqInt            : Builtin (ℤ × ℤ)
  | NeqInt           : Builtin (ℤ × ℤ)
  | GTInt            : Builtin (ℤ × ℤ)
  | GEQInt           : Builtin (ℤ × ℤ)
  | LTInt            : Builtin (ℤ × ℤ)
  | LEQInt           : Builtin (ℤ × ℤ)
  (* If *)
  | IfElse           : Builtin (ℤ × ℤ)
  (* Decimal ops *)
  (* Decimal add *)
  | AddDec           : Builtin (ℤ × ℤ)
  (* Decimal num *)
  | SubDec           : Builtin (ℤ × ℤ)
  | DivDec           : Builtin (ℤ × ℤ)
  | MulDec           : Builtin (ℤ × ℤ)
  | NegateDec        : Builtin (ℤ × ℤ)
  | AbsDec           : Builtin (ℤ × ℤ)
  (* Decimal rounding ops *)
  | RoundDec         : Builtin (ℤ × ℤ)
  | CeilingDec       : Builtin (ℤ × ℤ)
  | FloorDec         : Builtin (ℤ × ℤ)
  (* Decimal rounding ops *)
  | ExpDec           : Builtin (ℤ × ℤ)
  | LnDec            : Builtin (ℤ × ℤ)
  | LogBaseDec       : Builtin (ℤ × ℤ)
  | SqrtDec          : Builtin (ℤ × ℤ)
  (* Decimal Show *)
  | ShowDec          : Builtin (ℤ × ℤ)
  (* Decimal Equality *)
  | EqDec            : Builtin (ℤ × ℤ)
  | NeqDec           : Builtin (ℤ × ℤ)
  (* Decimal ord *)
  | GTDec            : Builtin (ℤ × ℤ)
  | GEQDec           : Builtin (ℤ × ℤ)
  | LTDec            : Builtin (ℤ × ℤ)
  | LEQDec           : Builtin (ℤ × ℤ)
  (* Bool Comparisons *)
  | AndBool          : Builtin (ℤ × ℤ)
  | OrBool           : Builtin (ℤ × ℤ)
  | NotBool          : Builtin (ℤ × ℤ)
  (* other bool ops *)
  | EqBool           : Builtin (ℤ × ℤ)
  | NeqBool          : Builtin (ℤ × ℤ)
  | ShowBool         : Builtin (ℤ × ℤ)
  (* String Equality *)
  | EqStr            : Builtin (ℤ × ℤ)
  | NeqStr           : Builtin (ℤ × ℤ)
  (* String Ord *)
  | GTStr            : Builtin (ℤ × ℤ)
  | GEQStr           : Builtin (ℤ × ℤ)
  | LTStr            : Builtin (ℤ × ℤ)
  | LEQStr           : Builtin (ℤ × ℤ)
   (* String Add *)
  | AddStr           : Builtin (ℤ × ℤ)
  (* String ListLike *)
  | ConcatStr        : Builtin (ℤ × ℤ)
  | DropStr          : Builtin (ℤ × ℤ)
  | TakeStr          : Builtin (ℤ × ℤ)
  | LengthStr        : Builtin (ℤ × ℤ)
  | ReverseStr       : Builtin (ℤ × ℤ)
  (* String Show *)
  | ShowStr          : Builtin (ℤ × ℤ)
  (* Object equality *)
  | EqObj            : Builtin (ℤ × ℤ)
  | NeqObj           : Builtin (ℤ × ℤ)
  (* List Equality *)
  | EqList           : Builtin (ℤ × ℤ)
  | NeqList          : Builtin (ℤ × ℤ)
  (* List Ord *)
  | GTList           : Builtin (ℤ × ℤ)
  | GEQList          : Builtin (ℤ × ℤ)
  | LTList           : Builtin (ℤ × ℤ)
  | LEQList          : Builtin (ℤ × ℤ)
  (* List Show *)
  | ShowList         : Builtin (ℤ × ℤ)
  (* List Add *)
  | AddList          : Builtin (ℤ × ℤ)
  (* ListLike List *)
  | TakeList         : Builtin (ℤ × ℤ)
  | DropList         : Builtin (ℤ × ℤ)
  | LengthList       : Builtin (ℤ × ℤ)
  | ConcatList       : Builtin (ℤ × ℤ)
  | ReverseList      : Builtin (ℤ × ℤ)
  (* Misc list ops *)
  | FilterList       : Builtin (ℤ × ℤ)
  | DistinctList     : Builtin (ℤ × ℤ)
  | MapList          : Builtin (ℤ × ℤ)
  | ZipList          : Builtin (ℤ × ℤ)
  | FoldList         : Builtin (ℤ × ℤ)
  (* Unit ops *)
  | EqUnit           : Builtin (ℤ × ℤ)
  | NeqUnit          : Builtin (ℤ × ℤ)
  | ShowUnit         : Builtin (ℤ × ℤ)
  (* Others *)
  | Enforce          : Builtin (ℤ × ℤ)
  | EnforceOne       : Builtin (ℤ × ℤ)
  | Enumerate        : Builtin (ℤ × ℤ)
  | EnumerateStepN   : Builtin (ℤ × ℤ).
